"""Helpers module."""


def sum_chars(x, y):
    """Sum the value of two string characters.

    Args:
        x (char): string of the integer.
        y (char): string of the integer.

    Returns:
        (int): sum of the integer value of `x` and integer value of `y`.
    """
    return int(x) + int(y)
